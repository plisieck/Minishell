#include "libft.h"

char	*ft_strdup(const char *str)
{
	size_t	len;
	char	*new;
	int		i;

	len = ft_strlen(str);
	new = (char *)malloc(sizeof(char) * len + 1);
	if (!new)
		return (NULL);
	i = 0;
	while (str[i])
	{
		new[i] = str[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
